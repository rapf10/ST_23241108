# lebh dari >
# kurang dari <
# lebih dari sama dengan >=
# kurang dari sama dengan <=
# sama dengan ==
# tidak sama dengan!=
# is
# is not


a = 3
b = 6
# lebih dari >
hasil = a > b
print (a, ">", b, "=", hasil)
#kurang dari <
hasil = a < b
print (a, "<", b, "=", hasil)

# lebih dari sama dengan >=
hasil = a >= 2
print (a, ">=", 2, "=", hasil)

# kurang dari sama dengan
hasil = a <= 4
print (a, "<=", 4, "=", hasil)

# sama dengan ==
hasil = a == 3
print (a, "==", 3, "=", hasil)

# tidak sama dengan !=
hasil = a != b
print (a, "!=", b, "=", hasil)

# is
hasil = a is 3
print (a, "is", 3, "=", hasil)

# is not
hasil = a is not b
print (a, "is not", b, "=", hasil)